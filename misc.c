/**
 * max:
 * 
 * @a: A number
 * @b: A number
 *
 * Returns: The maximum number among @a and @b.
 *
 **/
int max(int a, int b) {
	return a > b ? a : b;
}

/**
 * min:
 * 
 * @a: A number
 * @b: A number
 *
 * Returns: The minimum number among @a and @b.
 *
 **/
int min(int a, int b) {
	return a < b ? a : b;
}
